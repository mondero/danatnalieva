// --- ТАЙМЕР (5 ФЕВРАЛЯ 2026) ---
const startDate = new Date("2026-02-05T00:00:00").getTime();

function updateTimer() {
    const now = new Date().getTime();
    const distance = now - startDate;

    const days = Math.floor(distance / (1000 * 60 * 60 * 24));
    const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((distance % (1000 * 60)) / 1000);

    const dEl = document.getElementById("days");
    if (dEl) {
        dEl.innerText = days.toString().padStart(2, '0');
        document.getElementById("hours").innerText = hours.toString().padStart(2, '0');
        document.getElementById("minutes").innerText = minutes.toString().padStart(2, '0');
        document.getElementById("seconds").innerText = seconds.toString().padStart(2, '0');
    }
}
setInterval(updateTimer, 1000);
updateTimer();

// --- КОЛОДА КАРТОЧЕК (100 ПРИЧИН) ---
const deck = document.getElementById('reasons-deck');

function createCard(index) {
    if (index < 0 || !reasons[index]) return null;
    const card = document.createElement('div');
    card.className = 'reason-card';
    card.innerHTML = `
        <h4>Причина №${index + 1}</h4>
        <p>${reasons[index]}</p>
    `;

    card.addEventListener('click', () => {
        card.style.transform = 'translateX(200%) rotate(30deg)';
        card.style.opacity = '0';
        setTimeout(() => {
            card.remove();
            if (deck.children.length === 0) renderDeck(); 
        }, 500);
    });
    return card;
}

function renderDeck() {
    if (!deck) return;
    for (let i = 0; i < 3; i++) {
        const cardIndex = Math.floor(Math.random() * reasons.length);
        const card = createCard(cardIndex);
        if (card) deck.prepend(card);
    }
}
renderDeck();

